class Utilidades {
  static String nombreMes(var mes) {
    switch (mes) {
      case "01":
        {
          return 'Ene';
        }
        break;
      case "02":
        {
          return 'Feb';
        }
        break;
      case "03":
        {
          return 'Mar';
        }
        break;
      case "04":
        {
          return 'Abr';
        }
        break;
      case "05":
        {
          return 'May';
        }
        break;
      case "06":
        {
          return 'Jun';
        }
        break;
      case "07":
        {
          return 'Jul';
        }
        break;
      case "08":
        {
          return 'Ago';
        }
        break;
      case "09":
        {
          return 'Sep';
        }
        break;
      case "10":
        {
          return 'Oct';
        }
        break;
      case "11":
        {
          return 'Nov';
        }
        break;
      case "12":
        {
          return 'Dic';
        }
        break;
      default:
        {
          return 'Mes';
        }
    }
  }
}
